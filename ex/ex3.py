# + plus does addition
# - minus does subtraction
# / slash does division
# * asterisk does multiplication
# % percent does percentages
# < less than
# > greater than
# <= less than equal to
# >= greater than equal to

print ("I will now count my chickens:")
# the print function had to be done twice to get the number to print.
print ("Hens"), print (25 + 30 / 6)
print ("Roosters"), print (100 - 25 * 3 % 4)

